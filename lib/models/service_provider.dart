class ServiceProvider {
  final String name;
  final String role;
  final String phone;
  final String address;
  final int reviews;
  final bool isOnline;
  final String imageUrl;

  ServiceProvider({
    required this.name,
    required this.role,
    required this.phone,
    required this.address,
    required this.reviews,
    required this.isOnline,
    required this.imageUrl,
  });
}
